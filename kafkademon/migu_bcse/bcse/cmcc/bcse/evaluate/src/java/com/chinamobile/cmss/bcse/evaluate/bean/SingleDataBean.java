package com.chinamobile.cmss.bcse.evaluate.bean;

import java.util.List;

public class SingleDataBean {

	private String searchWord;
	
	private String dataSeachWord;
	
	private List<String>  dataIds;
	
	private List<String>  dataNames;
	
	private List<String>  ids;
	
	private List<String>  names;

	public String getSearchWord() {
		return searchWord;
	}

	public void setSearchWord(String searchWord) {
		this.searchWord = searchWord;
	}

	public String getDataSeachWord() {
		return dataSeachWord;
	}

	public void setDataSeachWord(String dataSeachWord) {
		this.dataSeachWord = dataSeachWord;
	}

	public List<String> getDataIds() {
		return dataIds;
	}

	public void setDataIds(List<String> dataIds) {
		this.dataIds = dataIds;
	}

	public List<String> getDataNames() {
		return dataNames;
	}

	public void setDataNames(List<String> dataNames) {
		this.dataNames = dataNames;
	}

	public List<String> getIds() {
		return ids;
	}

	public void setIds(List<String> ids) {
		this.ids = ids;
	}

	public List<String> getNames() {
		return names;
	}

	public void setNames(List<String> names) {
		this.names = names;
	}

}
